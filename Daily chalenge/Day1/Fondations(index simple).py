mot = "Python"

print("\n Affiche la première lettre du mot")
print(f"\n {mot[0]}")

print("\n Affiche la deuxième lettre")
print(f"\n {mot[1]}")

print("\n Affiche la dernière lettre")
print(f"\n {mot[-1]}")

print("\n Affiche les trois premières lettres")
print(f"\n {mot[:3]}")

print("\n Affiche les trois dernières lettres")
print(f"\n {mot[-3:]}")

print("\n Affiche le mot en }ordre inversé")
print(f"\n {mot[::-1]}")

print("\n Affiche uniquement les lettres y, t, o (avec les index)")
print(f"\n {mot[1], mot[2], mot[4]}")
